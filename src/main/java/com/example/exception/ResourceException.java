package com.example.exception;

/**
 * Custom exception if the input stream contains a malformed Unicode escape sequence.
 */
public class ResourceException extends Exception {
    public ResourceException() {
    }

    public ResourceException(String message) {
        super(message);
    }

    public ResourceException(String message, Throwable cause) {
        super(message, cause);
    }

    public ResourceException(Throwable exception) {
        super(exception);
    }

    public ResourceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}